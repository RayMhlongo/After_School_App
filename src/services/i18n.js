export const WEEKDAY_KEYS = [
  'monday',
  'tuesday',
  'wednesday',
  'thursday',
  'friday',
  'saturday',
  'sunday'
];

export function createTranslator(language = 'en') {
  const dictionary = translations[language] || translations.en;

  return function translate(key, params = {}) {
    const entry = dictionary[key] ?? translations.en[key] ?? key;

    if (typeof entry !== 'string') {
      return key;
    }

    return entry.replace(/\{(\w+)\}/g, (_, token) => `${params[token] ?? ''}`);
  };
}

export function getWeekdayLabel(dayOfWeek, language = 'en') {
  const t = createTranslator(language);
  return t(WEEKDAY_KEYS[dayOfWeek - 1]);
}

export function getActivityName(activity, language = 'en') {
  if (!activity) {
    return '';
  }

  return language === 'af'
    ? activity.nameAf || activity.nameEn
    : activity.nameEn || activity.nameAf;
}

export function getMealLabel(meal, language = 'en') {
  if (!meal) {
    return '';
  }

  return language === 'af' ? meal.labelAf || meal.labelEn : meal.labelEn || meal.labelAf;
}
